 #Usandolos métodos de cadenas o strings

"""Métodos de separación de strings con .split()"""

cadena = "Python para la predicción de fraudes bancarios"

cadena_sin_espacios = cadena.split()

print("La cadena separada ahora es {}".format(cadena_sin_espacios))

print(cadena_sin_espacios)

for palabra in cadena_sin_espacios:

    print(palabra)



