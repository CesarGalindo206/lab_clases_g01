#Usando los métodos de cadenas o strings

#Método de Strings 1

mi_cadena = "Machine learning con Python"

"""Convirtiendo a mayúsculas mi cadena con .upper()"""

print("Mi cadena transformada a mayúsculas es : {}".format(mi_cadena.upper()))

"""Convirtiendo a minúsculas mi cadena  con .lower()"""

print("Mi cadena transformada a minúsculas es : {}".format(mi_cadena.lower()))

"""Convirtiendo a un tipo título .title()"""

print("Mi cadena a forma de título es la siguiente : {}".format(mi_cadena.title()))
